#program konversi fahrenheit
print("\nPROGRAM KONVERSI FAHRENHEIT\n")
fahrenheit = float(input("Masukan suhu fahrenheit    : "))
celcius = (5/9)*(fahrenheit-32)
reamur = 4/9*(fahrenheit-32)
kelvin = celcius+273
print("\nSuhu fahrenheit            : ",fahrenheit,"fahrenheit\n")
print("Suhu celcius               : ",celcius,"celcius\n")
print("Suhu reamur                : ",reamur,"reamur\n")
print("Suhu kelvin                : ",kelvin,"kelvin\n")
