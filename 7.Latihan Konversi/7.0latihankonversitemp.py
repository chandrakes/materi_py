print("\nPROGRAM KONVERSI TEMPERATUR CELCIUS\n")
celcius = float(input("Masukan suhu celcius = "))
reamur = (4/5)*celcius
fahrenheit = ((9/5)*celcius)+32
kelvin = celcius + 273
print("Suhu dalam Celcius     =",celcius,"Celcius")
print("Suhu dalam Reamur      =",reamur,"Reamur")
print("Suhu dalam Fahrenheit  =",fahrenheit,"Fahreheit")
print("Suhu dalam kelvin      =",kelvin,"kelvin")
